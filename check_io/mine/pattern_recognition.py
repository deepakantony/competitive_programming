def examine( image, row, col, pattern ):
    for r in range(len(pattern)):
        for c in range(len(pattern[0])):
            if not (0 <= row+r < len(image) and 0 <= col+c < len(image[0]) and image[row+r][col+c] == pattern[r][c]):
                return False
    return True
    

def checkio(pattern, image):
    if not image: return image
    result = [ [c for c in r ] for r in image ]
    for row in range(len(image)):
        for col in range(len(image[0])):
            if examine( image, row, col, pattern ):
                for r in range(len(pattern)):
                    for c in range(len(pattern[0])):
                        image[row+r][col+c] += 2

    return image

